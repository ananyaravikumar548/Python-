import numpy as npy
arr_1 = npy.array([[1., 2., 3.], [4., 5., 6.]])
arr_2 = npy.array([[7., 8., 9.], [1., 3., 5.]])
print("Original array:\n", arr_1, arr_2)
print("Element-wise addition:\n", arr_1 + arr_2)
print("Element-wise subtraction:\n", arr_1 - arr_2)
print("Element-wise multiplication:\n", arr_1 * arr_2) # np.
